static void LCD_Write_Cmd(u8 cmd)
{
	LCD_DC(0);
	MX_SPI1_Send(&cmd, 1);   
}

static void LCD_Write_Data(u8 data)
{
	LCD_DC(1);
	MX_SPI1_Send(&data, 1);	
}

void LCD_Address_Set(u16 x1, u16 y1, u16 x2, u16 y2)
{
#if LCD_DISP_MOVE
	u16 x1_new,x2_new,y1_new,y2_new;

	x1_new = x1;
	x2_new = x2;
	y1_new = y1;
	y2_new = y2;
	if(x1_new > LCD_DISP_MOVE_LEFT)
	{
		x1_new -=  LCD_DISP_MOVE_LEFT;
		x2_new -= LCD_DISP_MOVE_LEFT;
	}
	if(y1_new > LCD_DISP_MOVE_TOP)
	{
		y1_new -= LCD_DISP_MOVE_TOP;
		y2_new -= LCD_DISP_MOVE_TOP;
	}	
	
	LCD_Write_Cmd(0x2a);
	LCD_Write_Data(x1_new>> 8);
	LCD_Write_Data(x1_new);
	LCD_Write_Data(x2_new>> 8);
	LCD_Write_Data(x2_new);

	LCD_Write_Cmd(0x2b);
	LCD_Write_Data(y1_new>> 8);
	LCD_Write_Data(y1_new);
	LCD_Write_Data(y2_new>> 8);
	LCD_Write_Data(y2_new);

	LCD_Write_Cmd(0x2C);
#else
	LCD_Write_Cmd(0x2a);
	LCD_Write_Data(x1 >> 8);
	LCD_Write_Data(x1);
	LCD_Write_Data(x2 >> 8);
	LCD_Write_Data(x2);

	LCD_Write_Cmd(0x2b);
	LCD_Write_Data(y1 >> 8);
	LCD_Write_Data(y1);
	LCD_Write_Data(y2 >> 8);
	LCD_Write_Data(y2);

	LCD_Write_Cmd(0x2C);
#endif	
}


/**
 * 以一种颜色清空LCD屏
 *
 * @param   color	清屏颜色
 *
 * @return  void
 */
void LCD_Clear(u16 color)
{
	LCD_Fill(0, 0,LCD_WIDTH,LCD_HEIGHT,color);
}
void LCD_Fill(u16 x_start, u16 y_start, u16 x_end, u16 y_end, u16 color)
{
    u16 i = 0;
    u32 size = 0, size_remain = 0;

    size = (x_end - x_start + 1) * (y_end - y_start + 1) * 2;

    if(size > LCD_BUFFER_LEN)
    {
        size_remain = size - LCD_BUFFER_LEN;
        size = LCD_BUFFER_LEN;
    }

    LCD_Address_Set(x_start, y_start, x_end, y_end);

    while(1)
    {
        for(i = 0; i < size / 2; i++)
        {
            LCD_Buffer[2 * i] = color >> 8;
            LCD_Buffer[2 * i + 1] = color;
        }

        LCD_DC(1);
        MX_SPI1_Send(LCD_Buffer, size);

        if(size_remain == 0)
            break;

        if(size_remain > LCD_BUFFER_LEN)
        {
            size_remain = size_remain - LCD_BUFFER_LEN;
        }

        else
        {
            size = size_remain;
            size_remain = 0;
        }
    }
}

