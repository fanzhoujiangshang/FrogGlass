/**
 * @brief	在指定位置开始显示一个字符串
 *
 * @param   x 		显示横坐标
 * @param   y		显示纵坐标
 * @param   width	显示区域宽度
 * @param   height	显示区域高度
 * @param   str  	字符串
 * @param   size 	字体大小
 *
 * @return 	void
 */
void Show_Str(u16 x, u16 y, u16 width, u16 height, const u8*str, u8 size)
{
    u16 x0 = x;
    u16 y0 = y;
    u8 bHz = 0;   //字符或者中文

    while(*str != 0) //数据未结束
    {
        if(!bHz)
        {
            if(*str > 0x80)bHz = 1; //中文

            else              //字符
            {
                if(x > (x0 + width - size / 2)) //换行
                {
                    y += size;
                    x = x0;
                }

                if(y > (y0 + height - size))break; //越界返回

                if(*str == 13) //换行符号
                {
                    y += size;
                    x = x0;
                    str++;
                }

                else LCD_ShowChar(x, y, *str, size); //有效部分写入

                str++;
                x += size / 2; //字符,为全字的一半
            }
        }

        else //中文
        {
        
            bHz = 0; //有汉字库

            if(x > (x0 + width - size)) //换行
            {
                y += size;
                x = x0;
            }

            if(y > (y0 + height - size))break; //越界返回

            Show_Font(x, y, str, size); //显示这个汉字,空心显示
            str += 2;
            x += size; //下一个汉字偏移
            
        }
    }
}

